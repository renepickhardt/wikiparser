package de.renepickhardt.imessages.wikiparser.dataTypes;

/**
 * <p>
 * A user Class to store Wikipedia contributors. Just a POJO with an ID and a
 * name.
 * <p>
 * @author mruster
 */
public class User extends AbstractCSVPrintableElement {

	protected String name;
	protected String id;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}
}
